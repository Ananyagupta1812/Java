import java.util.*;
//Code to print star pattern.
public class Main {
    public static void main(String[] args) {
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
    }
}

// Code to print solid rectangle pattern
public class Main {
    public static void main(String[] args) {
        int n=4;
        int m=5;
        for(int i=1;i<=n;i++) {
            for(int j=1;j<=m;j++) {
                System.out.println("*");
            }
            System.out.println("\n");
        }
    }
}
